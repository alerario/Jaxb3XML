    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        Sessao ss = new Sessao();
        Chat ct = new Chat();
        Mensagem msg = new Chat.Mensagem();
        Participante p1 = new Participante();
        Participante p2 = new Participante();
        p1.setId(1);
        p1.setNome("Alexandre");
        p2.setId(2);
        p2.setNome("Laura");
        msg.setCorpo("Texto principal");
        try {
            XMLGregorianCalendar xmlGregorianCalendar = DatatypeFactory.newInstance().newXMLGregorianCalendar(new GregorianCalendar());
            msg.setData(xmlGregorianCalendar);
        } catch (DatatypeConfigurationException ex) {
            Logger.getLogger(Principal.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        ct.getParticipante().add(p2);
        ct.getParticipante().add(p1);
        ct.setMensagem(msg);
        
        ss.getChat().add(ct);
        try {
            JAXBContext context = JAXBContext.newInstance(ObjectFactory.class);
            String txt = "";
            context.createMarshaller().marshal(ss, System.out);
            StringWriter sw = new StringWriter();
            context.createMarshaller().marshal(ss, sw);

            txt = sw.toString();
            jTextArea1.setText(txt);
        } catch (JAXBException ex) {
            Logger.getLogger(Principal.class.getName()).log(Level.SEVERE, null, ex);
        }


    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String txt = jTextArea1.getText();
        try {
            JAXBContext context = JAXBContext.newInstance(ObjectFactory.class);

            Unmarshaller u = context.createUnmarshaller();
            StringReader reader = new StringReader(txt);
            Sessao sess = (Sessao) u.unmarshal(reader);
            String  txt2 = "";
            for (Chat chat : sess.getChat()) {
                txt2 +="#################";
                txt2 += "\nfrom: " + chat.getParticipante().get(0).getNome();
                txt2 += "\nto: " + chat.getParticipante().get(1).getNome();
                txt2 += "\n"+chat.getMensagem().getData();
                txt2 += "\n" + chat.getMensagem().getCorpo() + "\n";
            }
            jTextArea1.setText(txt2);
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }


    }//GEN-LAST:event_jButton2ActionPerformed

